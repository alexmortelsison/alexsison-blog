function addVariablesForColors({ addBase, theme }: any) {
  const colors = theme("colors") || {}; // Fallback to an empty object if undefined
  const allColors = flattenColorPalette(colors); // Flatten the color palette
  const newVars = Object.fromEntries(
    Object.entries(allColors).map(([key, val]) => [`--${key}`, val])
  );

  addBase({
    ":root": newVars,
  });
}

function flattenColorPalette(colors: Record<string, any>) {
  const result: Record<string, string> = {};

  function recurse(obj: any, prefix = "") {
    Object.keys(obj).forEach((key) => {
      const value = obj[key];
      const newKey = prefix ? `${prefix}-${key}` : key;
      if (typeof value === "string") {
        result[newKey] = value;
      } else {
        recurse(value, newKey);
      }
    });
  }

  recurse(colors);
  return result;
}
